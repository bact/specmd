FIXTURE_MODEL = Path(__file__).parent / "fixtures" / "model"


def _make_model(tmp_path: Path, yaml: str = "") -> Model:
    """Copy the fixture model into *tmp_path* and write *yaml* as specmd.yml."""
    dest = tmp_path / "model"
    shutil.copytree(FIXTURE_MODEL, dest)
    if yaml:
        (dest / "specmd.yml").write_text(yaml, encoding="utf-8")
    return Model(dest)


def _fake_cfg(**kwargs: Any) -> Any:
    """Minimal RunParams stand-in for generators that need autogen_header / all_as_dict."""
    return SimpleNamespace(
        autogen_header="Generated by specmd test",
        license="Community-Spec-1.0",
        all_as_dict={
            "autogen_header": "Generated by specmd test",
            "license": "Community-Spec-1.0",
        },
        **kwargs,
    )


