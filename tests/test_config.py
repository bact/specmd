class TestConfigLoading:
    def test_no_config_file_gives_empty_dict(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path)
        assert m.config == {}

    def test_empty_config_file_gives_empty_dict(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "")
        assert m.config == {}

    def test_config_file_parsed_as_dict(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "namespace-order:\n  - Core\n")
        assert isinstance(m.config, dict)
        assert "namespace-order" in m.config

    def test_unknown_keys_silently_preserved(self, tmp_path: Path) -> None:
        # Extra keys don't crash loading; they are simply ignored by generators.
        m = _make_model(tmp_path, "custom-key: some-value\n")
        assert m.config.get("custom-key") == "some-value"


