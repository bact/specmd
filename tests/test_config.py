class TestOntologyConfig:
    def _graph(self, tmp_path: Path, ontology_yaml: str):
        m = _make_model(tmp_path, f"namespace-order:\n  - Core\n{ontology_yaml}")
        g = gen_rdf_ontology(m)
        ont = URIRef(m.base_uri)
        return g, ont

    def test_default_preferred_namespace_prefix(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path)
        g = gen_rdf_ontology(m)
        prefixes = dict(g.namespaces())
        assert "spdx" in prefixes

    def test_custom_preferred_namespace_prefix(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "namespace-order:\n  - Core\nontology:\n  preferred-namespace-prefix: myns\n")
        g = gen_rdf_ontology(m)
        assert "myns" in dict(g.namespaces())

    def test_default_label(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "")
        labels = [str(o) for _, _, o in g.triples((ont, RDFS.label, None))]
        assert any("SPDX" in lbl for lbl in labels)

    def test_custom_label(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "ontology:\n  label: My Ontology\n")
        labels = [str(o) for _, _, o in g.triples((ont, RDFS.label, None))]
        assert "My Ontology" in labels

    def test_default_title(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "")
        titles = [str(o) for _, _, o in g.triples((ont, DCTERMS.title, None))]
        assert any("SPDX" in t for t in titles)

    def test_custom_title(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "ontology:\n  title: My Title\n")
        titles = [str(o) for _, _, o in g.triples((ont, DCTERMS.title, None))]
        assert "My Title" in titles

    def test_default_creator(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "")
        creators = [str(o) for _, _, o in g.triples((ont, DCTERMS.creator, None))]
        assert any("SPDX" in c for c in creators)

    def test_custom_creator(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "ontology:\n  creator: Acme Corp\n")
        creators = [str(o) for _, _, o in g.triples((ont, DCTERMS.creator, None))]
        assert "Acme Corp" in creators

    def test_default_license_is_uri(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "")
        licenses = list(g.objects(ont, DCTERMS.license))
        assert any(isinstance(lic, URIRef) for lic in licenses)

    def test_custom_license(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "ontology:\n  license: https://example.org/license\n")
        licenses = list(g.objects(ont, DCTERMS.license))
        assert URIRef("https://example.org/license") in licenses

    def test_custom_abstract(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "ontology:\n  abstract: Short abstract.\n")
        abstracts = [str(o) for _, _, o in g.triples((ont, DCTERMS.abstract, None))]
        assert "Short abstract." in abstracts

    def test_custom_copyright(self, tmp_path: Path) -> None:
        omg_ann = Namespace("https://www.omg.org/spec/Commons/AnnotationVocabulary/")
        g, ont = self._graph(tmp_path, "ontology:\n  copyright: Copyright Acme\n")
        copyrights = [str(o) for _, _, o in g.triples((ont, omg_ann.copyright, None))]
        assert "Copyright Acme" in copyrights

    def test_custom_references(self, tmp_path: Path) -> None:
        g, ont = self._graph(tmp_path, "ontology:\n  references: https://example.org/spec\n")
        refs = list(g.objects(ont, DCTERMS.references))
        assert URIRef("https://example.org/spec") in refs


