class TestNamespaceOrderConfig:
    def test_no_key_returns_all_alphabetically(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "plantuml:\n  title: irrelevant\n")
        assert m.namespace_order() == ["Core"]

    def test_explicit_list_honoured(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "namespace-order:\n  - Core\n")
        assert m.namespace_order() == ["Core"]

    def test_empty_list_excludes_all(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "namespace-order: []\n")
        assert m.namespace_order() == []

    def test_include_unlisted_false_by_default(self, tmp_path: Path, caplog: pytest.LogCaptureFixture) -> None:
        m = _make_model(tmp_path, "namespace-order: []\n")
        with caplog.at_level(logging.WARNING, logger="specmd.parse.model"):
            result = m.namespace_order()
        assert result == []
        assert "excluded" in caplog.text

    def test_include_unlisted_true_appends_remainder(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "namespace-order: []\ninclude-unlisted-namespaces: true\n")
        assert m.namespace_order() == ["Core"]

    def test_include_unlisted_true_suppresses_exclusion_warning(self, tmp_path: Path, caplog: pytest.LogCaptureFixture) -> None:
        m = _make_model(tmp_path, "namespace-order: []\ninclude-unlisted-namespaces: true\n")
        with caplog.at_level(logging.WARNING, logger="specmd.parse.model"):
            m.namespace_order()
        assert "excluded" not in caplog.text

    def test_unknown_namespace_in_list_warns(self, tmp_path: Path, caplog: pytest.LogCaptureFixture) -> None:
        m = _make_model(tmp_path, "namespace-order:\n  - Core\n  - Ghost\n")
        with caplog.at_level(logging.WARNING, logger="specmd.parse.model"):
            result = m.namespace_order()
        assert result == ["Core"]
        assert "Ghost" in caplog.text


