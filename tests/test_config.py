class TestRdfConfig:
    def _run_gen(self, tmp_path: Path, rdf_yaml: str):
        m = _make_model(tmp_path / "m", f"namespace-order:\n  - Core\n{rdf_yaml}")
        out = tmp_path / "out"
        out.mkdir()
        gen_rdf(m, out, _fake_cfg())
        return out

    def test_default_filename_stem(self, tmp_path: Path) -> None:
        out = self._run_gen(tmp_path, "")
        assert (out / "spdx-model.ttl").exists()

    def test_default_context_filename(self, tmp_path: Path) -> None:
        out = self._run_gen(tmp_path, "")
        assert (out / "spdx-context.jsonld").exists()

    def test_custom_filename(self, tmp_path: Path) -> None:
        out = self._run_gen(tmp_path, "rdf:\n  filename: my-onto\n")
        assert (out / "my-onto.ttl").exists()
        assert not (out / "spdx-model.ttl").exists()

    def test_custom_context_filename(self, tmp_path: Path) -> None:
        out = self._run_gen(tmp_path, "rdf:\n  context-filename: my-ctx\n")
        assert (out / "my-ctx.jsonld").exists()
        assert not (out / "spdx-context.jsonld").exists()

    def test_dot_file_uses_filename(self, tmp_path: Path) -> None:
        out = self._run_gen(tmp_path, "rdf:\n  filename: custom\n")
        assert (out / "custom.dot").exists()


