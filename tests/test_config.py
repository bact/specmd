class TestLicenseConfig:
    def test_license_from_config(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "license: Apache-2.0\n")
        assert m.spdx_license == "Apache-2.0"

    def test_license_falls_back_to_namespace_source(self, tmp_path: Path) -> None:
        # No explicit config key -- should pick up license from namespace source file.
        m = _make_model(tmp_path)
        # Fixture namespace file carries Community-Spec-1.0.
        assert m.spdx_license == "Community-Spec-1.0"

    def test_license_config_overrides_namespace_source(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path, "license: MIT\n")
        assert m.spdx_license == "MIT"

    def test_mkdocs_frontmatter_contains_license(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path / "m", "namespace-order:\n  - Core\nlicense: MIT\n")
        out = tmp_path / "out"
        out.mkdir()
        gen_mkdocs(m, out, _fake_cfg())
        # Pick any generated page and confirm frontmatter has the right license.
        page = next((out / "Core" / "Classes").glob("*.md"))
        content = page.read_text()
        assert content.startswith("---\n")
        assert "SPDX-License-Identifier: Community-Spec-1.0" in content

    def test_singlefile_frontmatter_contains_license(self, tmp_path: Path) -> None:
        m = _make_model(tmp_path / "m", "namespace-order:\n  - Core\nlicense: CC0-1.0\n")
        out = tmp_path / "out"
        out.mkdir()
        gen_singlefile(m, out, _fake_cfg())
        content = (out / "model.md").read_text()
        assert content.startswith("---\n")
        assert "SPDX-License-Identifier: CC0-1.0" in content

    def test_singlefile_no_hardcoded_community_spec(self, tmp_path: Path) -> None:
        # When config has a different license, old hardcoded string must not appear.
        m = _make_model(tmp_path / "m", "namespace-order:\n  - Core\nlicense: MIT\n")
        out = tmp_path / "out"
        out.mkdir()
        gen_singlefile(m, out, _fake_cfg())
        content = (out / "model.md").read_text()
        assert "Community-Spec-1.0" not in content
        assert "MIT" in content
